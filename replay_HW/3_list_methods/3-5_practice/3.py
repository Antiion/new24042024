shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300], ['педаль', 100], ['седло', 1500], ['рама', 12000], ['обод', 2000], ['шатун', 200], ['седло', 2700]]

request = input('Введите название детали: ')

summ, count = 0, 0
for i in shop:
    if i[0] == request:
        summ += i[1]
        count += 1

if count:
    print(f'Количество деталей: {count}\nОбщая стоимость: {summ}')
else:
    print('Такой детали в магазине нет')

