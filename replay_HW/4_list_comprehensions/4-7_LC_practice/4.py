alphabet = 'abcdefg'
print(alphabet[::])
print(alphabet[::-1])
print(alphabet[::2])
print(alphabet[1::2])
print(alphabet[:2:]) #все элементы до второго
print(alphabet[-1:0:-1])
print(alphabet[3:4:])#срез от 3 до 4
print(alphabet[-1:-4:-1][::-1])#последние 3 элемента
print(alphabet[3:5:])
print(alphabet[3:5:][::-1])
